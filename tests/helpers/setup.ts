  const authority = Keypair.generate();
  const alice     = Keypair.generate();
  const bob       = Keypair.generate();

  // Start bankrun with the pool program
  const context = await startAnchor(
    ".",   // Anchor workspace root
    [],    // extra programs (info_pool, governance etc. added if needed)
    [
      // Pre-fund wallets
      { address: authority.publicKey, info: { lamports: 100 * LAMPORTS_PER_SOL, data: Buffer.alloc(0), owner: SystemProgram.programId, executable: false } },
      { address: alice.publicKey,     info: { lamports: 100 * LAMPORTS_PER_SOL, data: Buffer.alloc(0), owner: SystemProgram.programId, executable: false } },
      { address: bob.publicKey,       info: { lamports: 100 * LAMPORTS_PER_SOL, data: Buffer.alloc(0), owner: SystemProgram.programId, executable: false } },
    ],
  );

  const provider = new BankrunProvider(context);
  const idl      = loadPoolIdl();

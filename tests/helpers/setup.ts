export async function getTokenBalance(
  context: ProgramTestContext,
  account: PublicKey,
): Promise<bigint> {
  const info = await context.banksClient.getAccount(account);
  if (!info) return 0n;
  // Parse SPL token account data (bytes 64-72 = amount u64 LE)
  const data = Buffer.from(info.data);
  if (data.length < 72) return 0n;
  return data.readBigUInt64LE(64);
}

// ── Pool-wide fee math helpers (mirror on-chain formula) ────────

// Bug #2 constants
export const ORACLE_PRICE_SCALE      = BigInt("1000000"); // 1e6
export const MAX_ORACLE_STALENESS_SLOTS = 150n;

/**
 * Compute pool_fps increment for a single swap (pre-Bug-#2, native units).
 *   Δfps = out_fee_amount × FEE_SCALE / pool_total_lp_deposited
 * NOTE: this is the BUGGY formula — kept for regression comparison in tests.
 */
export function computeFpsIncrement(
  outFeeAmount:         bigint,
  poolTotalLpDeposited: bigint,
): bigint {
  if (poolTotalLpDeposited === 0n) return 0n;
  return (outFeeAmount * FEE_SCALE) / poolTotalLpDeposited;
}

/**
 * USD-normalise a deposit amount (Bug #2 fix).
 *   amount_usd = amount × oracle_price / (ORACLE_PRICE_SCALE × 10^decimals)
 *
 * oracle_price encodes USD per whole token × ORACLE_PRICE_SCALE (1e6).
 *   e.g. SOL at $150 → oracle_price = 150_000_000
 *
 * If oracle_price = 0 (pre-first-price-push), falls back to native amount.
 */
export function computeAmountUsd(
  amount:      bigint,
  oraclePrice: bigint,
  decimals:    number,
): bigint {
  if (oraclePrice === 0n) return amount; // fallback: no oracle yet
  const tokenScale = 10n ** BigInt(decimals);
  return (amount * oraclePrice) / (ORACLE_PRICE_SCALE * tokenScale);
}

/**
 * USD-normalise an outgoing swap fee (Bug #2 fix, mirrors swap.rs step 10).
 *   fee_usd = out_fee_amount × oracle_price_out
 *             / (ORACLE_PRICE_SCALE × 10^decimals_out)
 */
export function computeFeeUsd(
  outFeeAmount:  bigint,
  oraclePriceOut: bigint,
  decimalsOut:    number,
): bigint {
  const tokenScale = 10n ** BigInt(decimalsOut);
  return (outFeeAmount * oraclePriceOut) / (ORACLE_PRICE_SCALE * tokenScale);
}

/**
 * USD-normalised pool_fps increment (Bug #2 fix).
 *   fps_inc = fee_usd × FEE_SCALE / pool_total_lp_deposited_usd
 */
export function computeFpsIncrementUsd(
  feeUsd:                   bigint,
  poolTotalLpDepositedUsd:  bigint,
): bigint {
  if (poolTotalLpDepositedUsd === 0n) return 0n;
  return (feeUsd * FEE_SCALE) / poolTotalLpDepositedUsd;
}

/**
 * LP's claimable fees in USD units (Bug #2 fix, mirrors handler_public_exit).
 *   claimable_usd = pending_fees_usd
 *                 + amount_usd × (pool_fps − fee_debt) / FEE_SCALE
 */
export function computeClaimableUsd(
  amountUsd:      bigint,
  poolFps:        bigint,
  feeDebt:        bigint,
  pendingFeesUsd: bigint,
): bigint {
  const fpsDelta    = poolFps > feeDebt ? poolFps - feeDebt : 0n;
  const accrued     = (amountUsd * fpsDelta) / FEE_SCALE;
  return pendingFeesUsd + accrued;
}

/**
 * Convert USD-valued claimable back to native tokens at current oracle price
 * (Bug #2 fix — mirrors the native-conversion step in handler_public_exit).
 *   claimable_native = claimable_usd × ORACLE_PRICE_SCALE × 10^decimals
 *                      / oracle_price
 *
 * Returns 0 if oracle_price = 0 (no price pushed yet — no fees to claim).
 */
export function computeClaimableNative(
  claimableUsd: bigint,
  oraclePrice:  bigint,
  decimals:     number,
): bigint {
  if (oraclePrice === 0n || claimableUsd === 0n) return 0n;
  const tokenScale = 10n ** BigInt(decimals);
  return (claimableUsd * ORACLE_PRICE_SCALE * tokenScale) / oraclePrice;
}

/**
 * Compute LP's claimable fees (original native-unit formula).
 *   claimable = pending_fees + amount × (pool_fps − fee_debt) / FEE_SCALE
 */
export function computeClaimable(
  lpAmount:    bigint,
  poolFps:     bigint,
  feeDebt:     bigint,
  pendingFees: bigint,
): bigint {
  const fpsDelta    = poolFps > feeDebt ? poolFps - feeDebt : 0n;
  const fullAccrued = (lpAmount * fpsDelta) / FEE_SCALE;
  return pendingFees + fullAccrued;
}

/**
 * Compute swap out_fee_amount.
 *   out_fee_amount = amount_out_before_fee × current_fee_bps / BPS_DENOMINATOR
 */
export function computeOutFee(amountOutBeforeFee: bigint, feeBps: bigint): bigint {
  return (amountOutBeforeFee * feeBps) / BPS_DENOMINATOR;
}

/**
 * Expected pool_fps after N swaps each contributing outFeeAmount.
 */
export function expectedPoolFpsAfterSwaps(
  swaps:                Array<{ outFeeAmount: bigint }>,
  poolTotalLpDeposited: bigint,
): bigint {
  return swaps.reduce((fps, s) => fps + computeFpsIncrement(s.outFeeAmount, poolTotalLpDeposited), 0n);
}

/**
 * Staleness check mirroring swap.rs Bug #3 guard.
 * Returns true if the price is fresh enough to accept the swap.
 *   fresh = oracle_price_slot > 0
 *           && current_slot - oracle_price_slot <= MAX_ORACLE_STALENESS_SLOTS
 */
export function isOracleFresh(oraclePriceSlot: bigint, currentSlot: bigint): boolean {
  if (oraclePriceSlot === 0n) return false; // never pushed
  return (currentSlot - oraclePriceSlot) <= MAX_ORACLE_STALENESS_SLOTS;
}
